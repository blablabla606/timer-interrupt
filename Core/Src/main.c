typedef enum {
    PROCESS_1,
    PROCESS_4,
} ProcessType;

void process1_run()
{
    switch (step)
    {
        case 0:
            D4_ON(); D5_ON();
            break;

        case 1:
            D4_OFF(); D5_OFF();
            break;

        case 2:
            D4_ON(); D5_OFF();
            break;

        case 3:
            D4_OFF(); D5_ON();
            break;

        case 4:
            D4_ON(); D5_ON();
            break;

        default:
            step = 0;
            return;
    }

    step++;
}

void process4_run()
{
    switch (step)
    {
        // lặp 3 lần
        case 0:
        case 2:
        case 4:
            D4_ON(); D5_OFF();
            break;

        case 1:
        case 3:
        case 5:
            D4_ON(); D5_ON();
            break;

        // delay 2s (4 * 0.5s)
        case 6:
        case 7:
        case 8:
        case 9:
            D4_OFF(); D5_OFF();
            break;

        // cuối
        case 10:
            D4_OFF(); D5_ON();
            break;

        case 11:
            D4_ON(); D5_ON();
            break;

        default:
            step = 0;
            return;
    }

    step++;
}



